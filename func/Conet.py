# coding:utf-8


n: list = [1, 2]


class Conet():
    def forin(obj, *args):
        for _ in obj:
            args

    def prints(*args):
        print(*args)

    def ifs(init, codes, *args):
        init
        if (args): {
            codes
        }


if __name__ == '__main__':
    Conet.forin(n, print('hello'), print('world'))
    Conet.ifs(int(0), Conet.prints('a', 1, {1, 2}), True)



